// Math.max() : retorna o maior argumento

console.log( 'Args: 1, 4, 6, 8, 13, 2 => ' + Math.max( 1, 4, 6, 8, 13, 2 ) );
console.log( 'Args: [ 1, 4, 6, 8, 13, 2 ] => ' + Math.max( [ 1, 4, 6, 8, 13, 2 ] ) );

/*
  Retorna o maior dos argumentos. Caso o argumento não possa ser convertido
  em um valor númerico, retorna NaN
*/
