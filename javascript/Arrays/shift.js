/*

  Array.shift()

  - Remove o primeiro elemento do array e o retorna
  - Caso o array esteja vazio, retorna undefined

*/

var colors = [ 'red', 'blue', 'green' ];

console.log( colors.shift() );
console.log( colors );


console.log( colors.shift() );
console.log( colors );

console.log( colors.shift() );
console.log( colors );

console.log( colors.shift() );
console.log( colors );
